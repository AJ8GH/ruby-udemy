# ---- Inheritance ----
# - a way for a class to obtain all methods from another class
# - avoids duplicate code across multiple classes
# - class inherited from is the superclass or parent class
# - class that inherits is the subclass or child classes
# - Ruby only supports single inheritance,
# - a class can only inherit from one specific superclass
# - but because each superclass can inherit from another superclass,
# - each class has all methods from all classes above it
# - is-a relationship. sub-class is always a time of superclass or any class above it
#
# ---- syntax ----
# subclass < superclass
